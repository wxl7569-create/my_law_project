# 导入标准库
# ══════════════════════════════════════════════════════════════
# 从dataclasses模块导入dataclass装饰器，用于简化类的定义
from dataclasses import dataclass


# ══════════════════════════════════════════════════════════════
# 上下文数据模型
# ══════════════════════════════════════════════════════════════

@dataclass
class context:
    """
    上下文模型，用于存储和传递上下文信息
    """
    user_id: str        # 用户ID
    session_id: str = ""     # 会话ID


# ══════════════════════════════════════════════════════════════
# 响应格式数据模型
# ══════════════════════════════════════════════════════════════

#使用dataclass定义agent的结构化相应模型，包含agent的名称、响应内容和上下文信息
@dataclass
class responseformat:
    answer: str        # 智能体的响应内容，必填
    tool_used: str = ""  # 工具名称，空串比 None 节省 token
    law_civil: str = ""  # 民法典内容
    search_results: str = ""  # 搜索结果
    confidence: float = 0.0  # 置信度 (0-1)，为后续评估和前端展示预留
