"""简单非法律对话的快速回复。

这里只覆盖非常短且意图明确的输入，法律问题仍然交给智能体和工具处理。
"""

# ══════════════════════════════════════════════════════════════
# 导入标准库
# ══════════════════════════════════════════════════════════════
from __future__ import annotations

import re

# ══════════════════════════════════════════════════════════════
# 辅助常量：标点符号正则
# ══════════════════════════════════════════════════════════════

_PUNCT_RE = re.compile(r"[\s，。！？!?,.；;：:、~～]+")

# ══════════════════════════════════════════════════════════════
# 快捷回复匹配数据集
# ══════════════════════════════════════════════════════════════

# ── 问候语集合 ──
_GREETINGS = {
    "你好",
    "您好",
    "嗨",
    "哈喽",
    "hello",
    "hi",
    "hey",
}

# ── 感谢语集合 ──
_THANKS = {
    "谢谢",
    "感谢",
    "多谢",
    "谢了",
    "thanks",
    "thankyou",
}

# ── 告别语集合 ──
_GOODBYES = {
    "再见",
    "拜拜",
    "退出",
    "结束",
    "bye",
    "goodbye",
}

# ── 能力询问短语集合 ──
_CAPABILITY_PHRASES = (
    "你是谁",
    "你能做什么",
    "你可以做什么",
    "你会做什么",
    "你可以干什么",
    "你能干什么",
    "有什么功能",
    "你的功能",
    "帮助",
    "help",
)


# ══════════════════════════════════════════════════════════════
# 辅助函数
# ══════════════════════════════════════════════════════════════

def _normalize(text: str) -> str:
    return _PUNCT_RE.sub("", text.strip().lower())


# ══════════════════════════════════════════════════════════════
# 对外接口：获取快捷回复
# ══════════════════════════════════════════════════════════════

def get_quick_reply(message: str) -> str | None:
    """简单消息直接返回回复，否则返回空值。"""
    raw = (message or "").strip()
    if not raw:
        return None

    normalized = _normalize(raw)
    if not normalized:
        return None

    # 保持保守策略：较长消息通常包含真实问题。
    if len(normalized) <= 8 and normalized in _GREETINGS:
        return "你好，我是法律咨询助手。你可以直接向我提问法律问题，也可以上传文档让我结合内容分析。"

    if len(normalized) <= 10 and normalized in _THANKS:
        return "不客气。还有其他法律问题可以继续问我。"

    if len(normalized) <= 10 and normalized in _GOODBYES:
        return "好的，本次咨询先到这里。需要时可以随时回来继续提问。"

    if len(normalized) <= 30 and any(phrase in normalized for phrase in _CAPABILITY_PHRASES):
        return (
            "我是法律咨询助手，可以帮助你：\n"
            "1. 解答民法典相关问题；\n"
            "2. 检索并引用相关法律条文；\n"
            "3. 分析你上传的法律文档；\n"
            "4. 在需要最新信息时补充网络检索。\n"
            "你可以直接输入具体问题，例如“什么是宣告失踪”。"
        )

    return None
